    ignore pool;
    (match value with
    | `L payload ->
      begin
        write_int32 buf 0;
        write_literal payload buf pool;
        ()
      end
    | `Container (item0, item1) ->
      begin
        write_int32 buf 1;
        write_container_operator item0 buf pool;
        (let (item0, item1, item2) = item1 in
          begin
            write_tok item0 buf pool;
            write_int32 buf (List.length item1);
            List.iter (fun item ->
              begin
                write_expr item buf pool;
                ()
              end
            ) item1;
            write_tok item2 buf pool;
            ()
          end);
        ()
      end
    | `Comprehension (item0, item1) ->
      begin
        write_int32 buf 2;
        write_container_operator item0 buf pool;
        (let (item0, item1, item2) = item1 in
          begin
            write_tok item0 buf pool;
            write_comprehension item1 buf pool;
            write_tok item2 buf pool;
            ()
          end);
        ()
      end
    | `Record (item0, item1, item2) ->
      begin
        write_int32 buf 3;
        write_tok item0 buf pool;
        write_int32 buf (List.length item1);
        List.iter (fun item ->
          begin
            write_field item buf pool;
            ()
          end
        ) item1;
        write_tok item2 buf pool;
        ()
      end
    | `Constructor (item0, item1) ->
      begin
        write_int32 buf 4;
        write_name item0 buf pool;
        (let (item0, item1, item2) = item1 in
          begin
            write_tok item0 buf pool;
            write_int32 buf (List.length item1);
            List.iter (fun item ->
              begin
                write_expr item buf pool;
                ()
              end
            ) item1;
            write_tok item2 buf pool;
