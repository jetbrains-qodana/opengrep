    | `ArgKwdOptional (item0, item1) ->
      begin
        collect_ident item0 pool;
        collect_expr item1 pool;
        ()
      end
    | `ArgType payload ->
      begin
        collect_type_ payload pool;
        ()
      end
    | `OtherArg (item0, item1) ->
