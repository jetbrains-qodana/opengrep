  begin
    ignore value;
    ignore pool;
    (match value with
    | `ImportFrom (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_module_name item1 pool;
        List.iter (fun item ->
          begin
            (let (item0, item1) = item in
              begin
                collect_ident item0 pool;
                (match item1 with
                | None -> ()
                | Some someValue ->
                  begin
                    collect_alias someValue pool;
                    ()
                  end);
                ()
              end);
            ()
          end
        ) item2;
        ()
      end
    | `ImportAs (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_module_name item1 pool;
        (match item2 with
        | None -> ()
        | Some someValue ->
          begin
            collect_alias someValue pool;
            ()
          end);
        ()
      end
    | `ImportAll (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_module_name item1 pool;
        collect_tok item2 pool;
        ()
      end
    | `Package (item0, item1) ->
      begin
        collect_tok item0 pool;
        collect_dotted_ident item1 pool;
        ()
      end
    | `PackageEnd payload ->
      begin
        collect_tok payload pool;
        ()
      end
    | `Pragma (item0, item1) ->
      begin
        collect_ident item0 pool;
        List.iter (fun item ->
          begin
            collect_any item pool;
            ()
          end
        ) item1;
        ()
      end
