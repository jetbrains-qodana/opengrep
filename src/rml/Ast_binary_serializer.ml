and collect_stmt (value : stmt) (pool : string_pool_builder) : unit =
  begin
    ignore value;
    ignore pool;
    (match value with
    | `ExprStmt (item0, item1) ->
      begin
        collect_expr item0 pool;
        collect_sc item1 pool;
        ()
      end
    | `Block (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        List.iter (fun item ->
          begin
            collect_stmt item pool;
            ()
          end
        ) item1;
        collect_tok item2 pool;
        ()
      end
    | `If (item0, item1, item2, item3) ->
      begin
        collect_tok item0 pool;
        collect_condition item1 pool;
        collect_stmt item2 pool;
        (match item3 with
        | None -> ()
        | Some someValue ->
          begin
            collect_stmt someValue pool;
            ()
          end);
        ()
      end
    | `While (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_condition item1 pool;
        collect_stmt item2 pool;
        ()
      end
    | `Return (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        (match item1 with
        | None -> ()
        | Some someValue ->
          begin
            collect_expr someValue pool;
            ()
          end);
        collect_sc item2 pool;
        ()
      end
    | `DoWhile (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_stmt item1 pool;
        collect_expr item2 pool;
        ()
      end
    | `For (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_for_header item1 pool;
        collect_stmt item2 pool;
        ()
      end
    | `Switch (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        (match item1 with
        | None -> ()
        | Some someValue ->
          begin
            collect_condition someValue pool;
            ()
          end);
        List.iter (fun item ->
          begin
            collect_case_and_body item pool;
            ()
          end
        ) item2;
        ()
      end
    | `Continue (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_label_ident item1 pool;
        collect_sc item2 pool;
        ()
      end
    | `Break (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_label_ident item1 pool;
        collect_sc item2 pool;
        ()
      end
    | `Label (item0, item1) ->
      begin
        collect_label item0 pool;
        collect_stmt item1 pool;
        ()
      end
    | `Goto (item0, item1) ->
      begin
        collect_tok item0 pool;
        collect_label item1 pool;
        ()
      end
    | `Throw (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_expr item1 pool;
        collect_sc item2 pool;
        ()
      end
    | `Try (item0, item1, item2, item3) ->
      begin
        collect_tok item0 pool;
        collect_stmt item1 pool;
        List.iter (fun item ->
          begin
            collect_catch item pool;
            ()
          end
        ) item2;
        (match item3 with
        | None -> ()
        | Some someValue ->
          begin
            collect_finally someValue pool;
            ()
          end);
        ()
      end
    | `WithUsingResource (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        List.iter (fun item ->
          begin
            collect_stmt item pool;
            ()
          end
        ) item1;
        collect_stmt item2 pool;
        ()
      end
    | `Assert (item0, item1, item2) ->
      begin
        collect_tok item0 pool;
        collect_arguments item1 pool;
        collect_sc item2 pool;
        ()
      end
    | `DefStmt (item0, item1) ->
      begin
        collect_entity item0 pool;
        collect_definition_kind item1 pool;
        ()
      end
    | `DirectiveStmt payload ->
      begin
        collect_directive payload pool;
        ()
      end
    | `OtherStmt (item0, item1) ->
      begin
        collect_todo_kind item0 pool;
        List.iter (fun item ->
          begin
            collect_any item pool;
            ()
          end
        ) item1;
        ()
      end
    );
    ()
  end


